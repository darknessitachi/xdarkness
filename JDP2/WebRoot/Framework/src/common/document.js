/**
 * @class Arguments
 * @property callee 引用当前正在执行的函数，它提供了一种匿名的递归调用能力
 * 
 * 	如果一个函数是作为构造函数执行，那么满足 arguments.callee == this.constructor
 *  将参数转换为数组：Array.apply(this, arguments);
 * 	例：用必包计算10的阶乘
 * 		(function(x){
 * 			return x > 1 ? x * arguments.callee(x-1) : 1;
 * 		})(10);
 */

